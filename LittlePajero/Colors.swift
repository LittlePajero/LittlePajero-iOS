import UIKit

extension UIColor {
    class var lpOrange: UIColor {
        return UIColor(red: 255.0 / 255.0, green: 80.0 / 255.0, blue: 8.0 / 255.0, alpha: 1.0)
    }
    
    class var lpGrey: UIColor {
        return UIColor(white: 113.0 / 255.0, alpha: 1.0)
    }
    
    class var lpWhite: UIColor {
        return UIColor(white: 229.0 / 255.0, alpha: 1.0)
    }
    
    class var lpWhite50: UIColor {
        return UIColor(white: 229.0 / 255.0, alpha: 0.5)
    }
    
    class var lpBackgroundWhite: UIColor {
        return UIColor(white: 250.0 / 255.0, alpha: 1.0)
    }
    
    class var lpBlack: UIColor {
        return UIColor(white: 58.0 / 255.0, alpha: 1.0)
    }
    
    class var lpMuteBlack: UIColor { 
        return UIColor(white: 192.0 / 255.0, alpha: 1.0)
    }
    
    class var lpInkOnWhite: UIColor {
        return UIColor(white: 192.0 / 255.0, alpha: 0.5)
    }
    
    class var lpBlueLine: UIColor {
        return UIColor(red: 0.0 / 255.0, green: 204.0 / 255.0, blue: 255 / 255.0, alpha: 1.0)
    }
    
    class var lptransparent: UIColor {
        return UIColor(white: 255.0 / 255.0, alpha: 0)
    }
}
